
IF OBJECT_ID('STATION') IS NOT NULL DROP TABLE STATION;


CREATE TABLE STATION (
    ID INT,
    CITY VARCHAR(21),
    STATE VARCHAR(2),
    LAT_N FLOAT,
    LONG_W FLOAT
);


